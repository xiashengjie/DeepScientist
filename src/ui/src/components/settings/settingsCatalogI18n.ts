export function translateSettingsHelpMarkdown(locale: Locale, value?: string | null) {
  if (!value) return ''
  if (locale !== 'zh') return value

  let next = value
  next = next.replace(
    /This page edits the main runtime file at `([^`]+)`\./g,
    '本页编辑主运行时文件 `$1`。'
  )
  next = next.replace(/This page edits `([^`]+)` directly\./g, '本页直接编辑 `$1`。')

  for (const [from, to] of settingsHelpZhReplacements) {
    next = next.split(from).join(to)
  }
  return next
}

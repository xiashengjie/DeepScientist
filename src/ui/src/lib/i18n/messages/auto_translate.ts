const cleanupText = (text: string) =>
  text
    .replace(/\s+([,.;:!?])/g, '$1')
    .replace(/\s+\)/g, ')')
    .replace(/\(\s+/g, '(')
    .replace(/\s{2,}/g, ' ')
    .trim()

const translateCore = (text: string, locale: AutoLocale): string => {
  const manualExact = MANUAL_POLISH_EXACT[text]?.[locale]
  if (manualExact) return manualExact

  const sourceExact = SOURCE_EXACT[text]?.[locale]
  if (sourceExact) return sourceExact

  const exact = EXACT[locale][text]
  if (exact) return exact

  for (const [pattern, handler] of PATTERNS[locale]) {
    const match = pattern.exec(text)
    if (match) return cleanupText(handler(match, locale))
  }

  let result = text
  for (const [source, target] of PHRASES[locale]) {
    result = result.replace(new RegExp(escapeRegExp(source), 'gi'), target)
  }

  return cleanupText(result)
}

export function autoTranslateMessages(
  source: Record<string, string>,
  locale: AutoLocale,
  overrides: Record<string, string> = {}
): Record<string, string> {
  const output: Record<string, string> = {}
  for (const [key, value] of Object.entries(source)) {
    if (Object.prototype.hasOwnProperty.call(overrides, key)) {
      output[key] = overrides[key]
      continue
    }
    const { protectedText, protectedParts } = protectText(value)
    output[key] = restoreText(translateCore(protectedText, locale), protectedParts)
  }
  return output
}

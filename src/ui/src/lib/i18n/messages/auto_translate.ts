const protectText = (text: string) => {
  const protectedParts: string[] = []
  const push = (value: string) => `@@${protectedParts.push(value) - 1}@@`
  const protectedText = text.replace(/(\{[a-zA-Z0-9_]+\}|`[^`]+`|https?:\/\/\S+)/g, (match) => push(match))
  return { protectedText, protectedParts }
}

